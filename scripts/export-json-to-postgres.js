const fs = require("fs");
const path = require("path");

const ROOT = path.join(__dirname, "..");
const DATA_DIR = path.join(ROOT, "storage");
const OUT_DIR = path.join(ROOT, "db", "generated");
const OUT_FILE = path.join(OUT_DIR, "seed.sql");

function readJson(filename, fallback) {
  const fullPath = path.join(DATA_DIR, filename);
  if (!fs.existsSync(fullPath)) return fallback;
  try {
    return JSON.parse(fs.readFileSync(fullPath, "utf8"));
  } catch (error) {
    console.warn(`[WARN] Could not parse ${filename}: ${error.message}`);
    return fallback;
  }
}

function sql(value) {
  if (value === null || value === undefined) return "NULL";
  return `'${String(value).replace(/'/g, "''")}'`;
}

function sqlJson(value) {
  return `${sql(JSON.stringify(value === undefined ? null : value))}::jsonb`;
}

function sqlBool(value) {
  return value ? "true" : "false";
}

function sqlNumber(value, fallback = 0) {
  const number = Number(value);
  return Number.isFinite(number) ? String(number) : String(fallback);
}

function sqlTimestamp(value) {
  const number = Number(value);
  const date = Number.isFinite(number) ? new Date(number) : new Date();
  return sql(date.toISOString());
}

function mediaKind(url, fallback = "file") {
  if (!url) return fallback;
  const ext = path.extname(String(url).toLowerCase());
  if ([".mp4", ".webm", ".ogg", ".mov"].includes(ext)) return "video";
  if ([".jpg", ".jpeg", ".png", ".gif", ".webp", ".ico"].includes(ext)) return "image";
  return fallback;
}

function insert(table, row) {
  const columns = Object.keys(row);
  const values = columns.map(column => row[column]);
  return `INSERT INTO ${table} (${columns.join(", ")}) VALUES (${values.join(", ")}) ON CONFLICT DO NOTHING;`;
}

function ensureUser(users, username) {
  if (!username || users[username]) return;
  users[username] = {
    password: null,
    about: "",
    metadata: { migratedStub: true }
  };
}

function addMedia(sqlLines, seenMedia, publicUrl, owner, extra = {}) {
  if (!publicUrl || seenMedia.has(publicUrl)) return;
  seenMedia.add(publicUrl);
  sqlLines.push(insert("media_assets", {
    owner_username: sql(owner),
    storage_key: sql(String(publicUrl).replace(/^\//, "")),
    public_url: sql(publicUrl),
    media_kind: sql(extra.mediaKind || mediaKind(publicUrl)),
    mime_type: sql(extra.mimeType || null),
    original_name: sql(extra.originalName || null),
    byte_size: extra.byteSize === undefined ? "NULL" : sqlNumber(extra.byteSize, 0),
    metadata: sqlJson(extra.metadata || {})
  }));
}

const users = readJson("users.json", {});
const messages = readJson("messages.json", []);
const directs = readJson("directs.json", []);
const groups = readJson("groups.json", []);
const notifications = readJson("notifications.json", []);
const tickets = readJson("sup.json", []);
const cyberbites = readJson("cyberbites.json", []);

messages.forEach(post => {
  ensureUser(users, post.username);
  (post.likes || []).forEach(username => ensureUser(users, username));
  (post.saves || []).forEach(username => ensureUser(users, username));
  (post.retweets || []).forEach(username => ensureUser(users, username));
});

directs.forEach(message => {
  ensureUser(users, message.sender);
  ensureUser(users, message.receiver);
});

groups.forEach(group => {
  ensureUser(users, group.owner || group.createdBy);
  (group.members || []).forEach(member => ensureUser(users, member.username || member));
});

notifications.forEach(notification => {
  ensureUser(users, notification.user || notification.recipient || notification.recipient_username);
  ensureUser(users, notification.actor || notification.from);
});

cyberbites.forEach(bite => {
  ensureUser(users, bite.username);
  (bite.likes || []).forEach(username => ensureUser(users, username));
  (bite.saves || []).forEach(username => ensureUser(users, username));
});

Object.entries(users).forEach(([, user]) => {
  (user.following || []).forEach(username => ensureUser(users, username));
  (user.followers || []).forEach(username => ensureUser(users, username));
});

tickets.forEach(ticket => {
  ensureUser(users, ticket.username || ticket.creator);
});

const sqlLines = [
  "-- Generated by scripts/export-json-to-postgres.js",
  `-- Generated at ${new Date().toISOString()}`,
  "BEGIN;",
  "SET CONSTRAINTS ALL DEFERRED;"
];
const seenMedia = new Set();

Object.entries(users).forEach(([username, user]) => {
  sqlLines.push(insert("app_users", {
    username: sql(username),
    password_hash: sql(user.password || user.passwordHash || null),
    pfp_url: sql(user.pfp || null),
    banner_url: sql(user.banner || user.bannerUrl || null),
    about: sql(user.about || ""),
    is_premium: sqlBool(user.isPremium === true),
    is_admin: sqlBool(username.startsWith("/admin") || user.isAdmin === true),
    two_factor_secret: sql(user.twoFactorSecret || null),
    two_factor_enabled: sqlBool(user.twoFactorEnabled === true),
    privacy_settings: sqlJson(user.privacySettings || {}),
    screen_time: sqlJson(user.screenTime || {}),
    cyberbite_stats: sqlJson(user.cyberbiteStats || {}),
    metadata: sqlJson(user.metadata || {})
  }));

  addMedia(sqlLines, seenMedia, user.pfp, username, { mediaKind: "avatar" });
  addMedia(sqlLines, seenMedia, user.banner || user.bannerUrl, username, { mediaKind: "banner" });

  (user.following || []).forEach(following => {
    sqlLines.push(insert("follows", {
      follower_username: sql(username),
      following_username: sql(following)
    }));
  });
});

messages.forEach(post => {
  addMedia(sqlLines, seenMedia, post.imageUrl, post.username, { mediaKind: post.isVideo ? "video" : mediaKind(post.imageUrl, "image") });
  sqlLines.push(insert("posts", {
    id: sqlNumber(post.id, Date.now()),
    author_username: sql(post.username),
    body: sql(post.message || ""),
    media_url: sql(post.imageUrl || null),
    is_video: sqlBool(post.isVideo === true),
    parent_id: post.parentId ? sqlNumber(post.parentId) : "NULL",
    retweet_of: post.retweetOf ? sqlNumber(post.retweetOf) : "NULL",
    quote_of: post.quoteOf ? sqlNumber(post.quoteOf) : "NULL",
    is_retweet: sqlBool(post.isRetweet === true),
    is_quote: sqlBool(post.isQuote === true),
    is_pinned: sqlBool(post.isPinned === true),
    pfp_url: sql(post.pfp || null),
    views: sqlNumber(post.views, 0),
    metadata: sqlJson(post),
    created_at: sqlTimestamp(post.timestamp)
  }));

  (post.likes || []).forEach(username => sqlLines.push(insert("post_interactions", {
    post_id: sqlNumber(post.id),
    username: sql(username),
    interaction_type: sql("like")
  })));
  (post.saves || []).forEach(username => sqlLines.push(insert("post_interactions", {
    post_id: sqlNumber(post.id),
    username: sql(username),
    interaction_type: sql("save")
  })));
  (post.retweets || []).forEach(username => sqlLines.push(insert("post_interactions", {
    post_id: sqlNumber(post.id),
    username: sql(username),
    interaction_type: sql("retweet")
  })));
});

directs.forEach(message => {
  addMedia(sqlLines, seenMedia, message.mediaUrl, message.sender, { mediaKind: message.isVideo ? "video" : mediaKind(message.mediaUrl, "image"), originalName: message.mediaName });
  sqlLines.push(insert("direct_messages", {
    legacy_id: sql(message.id || `${message.sender}:${message.receiver}:${message.timestamp}`),
    sender_username: sql(message.sender),
    receiver_username: sql(message.receiver),
    body: sql(message.message || ""),
    media_url: sql(message.mediaUrl || null),
    is_video: sqlBool(message.isVideo === true),
    media_name: sql(message.mediaName || null),
    status: sql(message.status || "sent"),
    reactions: sqlJson(message.reactions || {}),
    metadata: sqlJson(message),
    created_at: sqlTimestamp(message.timestamp)
  }));
});

groups.forEach(group => {
  sqlLines.push(insert("chat_groups", {
    id: sql(group.id || group.name),
    name: sql(group.name || "Group"),
    owner_username: sql(group.owner || group.createdBy || null),
    metadata: sqlJson(group),
    created_at: sqlTimestamp(group.createdAt || group.timestamp)
  }));

  (group.members || []).forEach(member => {
    const username = member.username || member;
    sqlLines.push(insert("group_members", {
      group_id: sql(group.id || group.name),
      username: sql(username),
      role: sql(member.role || "member"),
      joined_at: sqlTimestamp(member.joinedAt || group.createdAt || group.timestamp)
    }));
  });
});

notifications.forEach(notification => {
  const recipient = notification.user || notification.recipient || notification.recipient_username;
  sqlLines.push(insert("notifications", {
    id: sql(notification.id || `${recipient}:${notification.timestamp || Date.now()}:${notification.type || "notification"}`),
    recipient_username: sql(recipient),
    actor_username: sql(notification.actor || notification.from || null),
    notification_type: sql(notification.type || "notification"),
    post_id: notification.postId ? sqlNumber(notification.postId) : "NULL",
    body: sql(notification.message || notification.body || ""),
    is_read: sqlBool(notification.read === true || notification.isRead === true),
    metadata: sqlJson(notification),
    created_at: sqlTimestamp(notification.timestamp || notification.createdAt)
  }));
});

cyberbites.forEach(bite => {
  addMedia(sqlLines, seenMedia, bite.videoUrl, bite.username, {
    mediaKind: "video",
    originalName: bite.originalName,
    byteSize: bite.fileSize
  });
  sqlLines.push(insert("cyberbites", {
    id: sqlNumber(bite.id, Date.now()),
    author_username: sql(bite.username),
    video_url: sql(bite.videoUrl),
    caption: sql(bite.caption || ""),
    pfp_url: sql(bite.pfp || null),
    views: sqlNumber(bite.views, 0),
    file_size: bite.fileSize === undefined ? "NULL" : sqlNumber(bite.fileSize),
    original_name: sql(bite.originalName || null),
    is_premium: sqlBool(bite.isPremium === true),
    is_admin: sqlBool(bite.isAdmin === true),
    metadata: sqlJson(bite),
    created_at: sqlTimestamp(bite.timestamp)
  }));

  (bite.likes || []).forEach(username => sqlLines.push(insert("cyberbite_interactions", {
    cyberbite_id: sqlNumber(bite.id),
    username: sql(username),
    interaction_type: sql("like")
  })));
  (bite.saves || []).forEach(username => sqlLines.push(insert("cyberbite_interactions", {
    cyberbite_id: sqlNumber(bite.id),
    username: sql(username),
    interaction_type: sql("save")
  })));
});

tickets.forEach(ticket => {
  sqlLines.push(insert("support_tickets", {
    id: sql(ticket.id || `${ticket.username || "ticket"}:${ticket.timestamp || Date.now()}`),
    creator_username: sql(ticket.username || ticket.creator || null),
    subject: sql(ticket.subject || ""),
    category: sql(ticket.category || ""),
    status: sql(ticket.status || "open"),
    priority: sql(ticket.priority || null),
    messages: sqlJson(ticket.messages || []),
    metadata: sqlJson(ticket),
    created_at: sqlTimestamp(ticket.timestamp || ticket.createdAt),
    updated_at: sqlTimestamp(ticket.updatedAt || ticket.timestamp || ticket.createdAt)
  }));
});

sqlLines.push("COMMIT;");

if (!fs.existsSync(OUT_DIR)) fs.mkdirSync(OUT_DIR, { recursive: true });
fs.writeFileSync(OUT_FILE, `${sqlLines.join("\n")}\n`);

console.log(`[OK] Wrote ${path.relative(ROOT, OUT_FILE)}`);
console.log(`[OK] Users: ${Object.keys(users).length}`);
console.log(`[OK] Posts: ${messages.length}`);
console.log(`[OK] Direct messages: ${directs.length}`);
console.log(`[OK] Groups: ${groups.length}`);
console.log(`[OK] Notifications: ${notifications.length}`);
console.log(`[OK] CyberBites: ${cyberbites.length}`);
console.log(`[OK] Support tickets: ${tickets.length}`);
